package variablesAndDataTypes;

public class SecondJavaClass {
	
	public static void main(String[] args) {		
		int a=10;
		int b=20;		
		System.out.println(a);	
		
		//Arithmetic Operators
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(b/a);
		System.out.println(a*b);
		
		//Assignment Operator
		b=a;
		
		System.out.println(b);
		
	}

}
